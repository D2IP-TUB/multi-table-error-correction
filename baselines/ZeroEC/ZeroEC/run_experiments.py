#!/usr/bin/env python3
"""
Run ZeroEC experiments on all Quintet_3 datasets with different human_repair_num values
and aggregate the results.
Reads metrics directly from output.txt files generated by correction.py.
"""

import os
import sys

from experiment_utils import (aggregate_results, collect_existing_results,
                              extract_metrics_from_output)


def run_zeroec_experiment(dataset_name, dataset_path, human_repair_num, results_base_path, base_zeroec_path):
    """
    Run ZeroEC on a single dataset with specified human_repair_num.
    """
    clean_path = os.path.join(dataset_path, 'clean.csv')
    dirty_path = os.path.join(dataset_path, 'dirty.csv')
    detection_path = os.path.join(dataset_path, 'perfect_error_detection.csv')
    
    # Create output directory
    output_path = os.path.join(results_base_path, dataset_name, f"human_repair_{human_repair_num}")
    os.makedirs(output_path, exist_ok=True)
    
    print(f"\n{'='*80}")
    print(f"Running ZeroEC on {dataset_name} with human_repair_num={human_repair_num}")
    print(f"{'='*80}")
    
    # Prepare the command to run correction.py with modified parameters    
    cmd = [
        sys.executable,
        os.path.join(base_zeroec_path, 'correction.py'),
        '--clean_data_path', clean_path,
        '--dirty_data_path', dirty_path,
        '--detection_path', detection_path,
        '--output_path', output_path,
        '--human_repair_num', str(human_repair_num)
    ]
    
    import subprocess
    try:
        print(f"Dataset: {dataset_name}, human_repair_num: {human_repair_num}")
        print(f"Running: {' '.join(cmd)}")
        print(f"Output path: {output_path}")
        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0:
            print(f"Error running ZeroEC: {result.stderr}")
            return output_path, False
        return output_path, True  # Mark as executed
    except Exception as e:
        print(f"Exception running ZeroEC: {e}")
        return output_path, False


def main():
    """
    Main function to run experiments on all datasets.
    """
    # Configuration
    BASE_ZEROEC_PATH = '/home/fatemeh/LakeCorrectionBench/ZeroEC'
    DATASETS_PATH = '/home/fatemeh/LakeCorrectionBench/uk_open_data/merged'
    RESULTS_BASE_PATH = '/home/fatemeh/LakeCorrectionBench/ZeroEC/results/open_data_uk_pm_labeling_budget_10'
    
    # List of human_repair_num values to test
    HUMAN_REPAIR_NUMS = [10]
    
    # Get all datasets
    datasets = [d for d in os.listdir(DATASETS_PATH) 
                if os.path.isdir(os.path.join(DATASETS_PATH, d))]
    datasets.sort()
    
    print(f"Found {len(datasets)} datasets: {datasets}")
    print(f"Testing with human_repair_nums: {HUMAN_REPAIR_NUMS}")
    
    # Results storage
    all_results = []
    
    # Run experiments
    for dataset_name in datasets:
        dataset_path = os.path.join(DATASETS_PATH, dataset_name)
        skip_dataset = False
        for human_repair_num in HUMAN_REPAIR_NUMS:
            result_dir = os.path.join(RESULTS_BASE_PATH, dataset_name, f"human_repair_{human_repair_num}")
            if os.path.exists(result_dir):
                print(f"Skipping {dataset_name} (human_repair_num={human_repair_num}): results already exist at {result_dir}")
                skip_dataset = True
                continue
            output_path, executed = run_zeroec_experiment(
                dataset_name=dataset_name,
                dataset_path=dataset_path,
                human_repair_num=human_repair_num,
                results_base_path=RESULTS_BASE_PATH,
                base_zeroec_path=BASE_ZEROEC_PATH
            )
            if executed:
                # Extract metrics from output.txt
                metrics = extract_metrics_from_output(output_path)
                if metrics:
                    result = {
                        'dataset': dataset_name,
                        'human_repair_num': metrics['human_repair_num'],
                        'tp': metrics['tp'],
                        'fp': metrics['fp'],
                        'fn': metrics['fn'],
                        'total_errors': metrics['total_errors'],
                        'total_corrections': metrics['total_corrections'],
                        'precision': metrics['precision'],
                        'recall': metrics['recall'],
                        'f1': metrics['f1'],
                        'execution_time': metrics['execution_time']
                    }
                    all_results.append(result)
                    print(f"\nResults for {dataset_name} (human_repair_num={human_repair_num}):")
                    print(f"  TP: {metrics['tp']}, FP: {metrics['fp']}, FN: {metrics['fn']}")
                    print(f"  Precision: {metrics['precision']:.4f}, Recall: {metrics['recall']:.4f}, F1: {metrics['f1']:.4f}")
                    print(f"  Execution time: {metrics['execution_time']:.2f}s")
                else:
                    print(f"  WARNING: Could not extract metrics from output.txt")
    
    # Aggregate and save results
    if all_results:
        output_csv = os.path.join(RESULTS_BASE_PATH, '50_with_duplicates_results.csv')
        aggregate_results(all_results, output_csv)
    else:
        print("\n" + "="*80)
        print("NOTE: No experiments were executed.")
        print("You need to modify correction.py to accept command-line arguments first.")
        print("\nAlternatively, you can:")
        print("1. Run this script after manually executing correction.py for each dataset")
        print("2. Create a wrapper script that modifies the parameters in correction.py")
        print("3. Use the evaluation mode below to analyze existing results")
        print("="*80)


def evaluate_existing_results():
    """
    Evaluate existing results without running experiments.
    """
    DATASETS_PATH = '/home/fatemeh/LakeCorrectionBench/datasets/Quintet_3'
    RESULTS_BASE_PATH = '/home/fatemeh/LakeCorrectionBench/ZeroEC/results/Quintet_3'
    
    all_results = collect_existing_results(DATASETS_PATH, RESULTS_BASE_PATH)
    
    if all_results:
        output_csv = os.path.join(RESULTS_BASE_PATH, 'zeroec_quintet3_results.csv')
        aggregate_results(all_results, output_csv)
        return True
    else:
        print("No existing results found to evaluate.")
        return False


if __name__ == '__main__':
    import argparse
    
    parser = argparse.ArgumentParser(description='Run ZeroEC experiments on Quintet_3 datasets')
    parser.add_argument('--evaluate-only', action='store_true', 
                        help='Only evaluate existing results without running experiments')
    
    args = parser.parse_args()
    
    if args.evaluate_only:
        print("Evaluating existing results...")
        evaluate_existing_results()
    else:
        print("Running experiments...")
        main()
