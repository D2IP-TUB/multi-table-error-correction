#!/usr/bin/env python3
"""
Quick evaluation script for existing ZeroEC results.
Reads metrics directly from output.txt files generated by correction.py.
"""

import os

import pandas as pd
from experiment_utils import extract_metrics_from_output


def main():
    DATASETS_PATH = '/home/fatemeh/LakeCorrectionBench/datasets/Quintet_3'
    RESULTS_PATH = '/home/fatemeh/LakeCorrectionBench/ZeroEC-0-Shot/results/Quintet_3_zeroshot_full'
    
    results = []
    
    # Find all result directories
    if not os.path.exists(RESULTS_PATH):
        print(f"Results path not found: {RESULTS_PATH}")
        return
    
    for dataset in os.listdir(DATASETS_PATH):
        dataset_path = os.path.join(DATASETS_PATH, dataset)
        if not os.path.isdir(dataset_path):
            continue
        
        dataset_results = os.path.join(RESULTS_PATH, dataset)
        if not os.path.exists(dataset_results):
            continue
        
        # Check if results are directly in dataset folder (old structure)
        corrected_path_direct = os.path.join(dataset_results, 'corrections.csv')
        if os.path.exists(corrected_path_direct):
            metrics = extract_metrics_from_output(dataset_results)
            
            if metrics:
                results.append({
                    'dataset': dataset,
                    'human_repair_num': metrics['human_repair_num'],
                    'tp': metrics['tp'],
                    'fp': metrics['fp'],
                    'fn': metrics['fn'],
                    'total_errors': metrics['total_errors'],
                    'precision': round(metrics['precision'], 4),
                    'recall': round(metrics['recall'], 4),
                    'f1': round(metrics['f1'], 4),
                    'execution_time': round(metrics['execution_time'], 2)
                })
                
                print(f"✓ {dataset:15s} human_repair={metrics['human_repair_num']:2d}  "
                      f"P={metrics['precision']:.3f} R={metrics['recall']:.3f} F1={metrics['f1']:.3f}")
            else:
                print(f"✗ {dataset} (direct) - Could not extract metrics from output.txt")
        
        # Also check for human_repair_* subdirectories (new structure)
        for result_dir in os.listdir(dataset_results):
            result_dir_path = os.path.join(dataset_results, result_dir)
            if not os.path.isdir(result_dir_path) or not result_dir.startswith('human_repair_'):
                continue
            
            corrected_path = os.path.join(result_dir_path, 'corrections.csv')
            if not os.path.exists(corrected_path):
                print(f"Skipping {dataset}/{result_dir} - no corrections.csv")
                continue
            
            metrics = extract_metrics_from_output(result_dir_path)
            
            if metrics:
                results.append({
                    'dataset': dataset,
                    'human_repair_num': metrics['human_repair_num'],
                    'tp': metrics['tp'],
                    'fp': metrics['fp'],
                    'fn': metrics['fn'],
                    'total_errors': metrics['total_errors'],
                    'precision': round(metrics['precision'], 4),
                    'recall': round(metrics['recall'], 4),
                    'f1': round(metrics['f1'], 4),
                    'execution_time': round(metrics['execution_time'], 2)
                })
                
                print(f"✓ {dataset:15s} human_repair={metrics['human_repair_num']:2d}  "
                      f"P={metrics['precision']:.3f} R={metrics['recall']:.3f} F1={metrics['f1']:.3f}")
            else:
                print(f"✗ {dataset}/{result_dir} - Could not extract metrics from output.txt")
    
    if not results:
        print("\nNo results found to evaluate.")
        return
    
    # Save results
    df = pd.DataFrame(results)
    df = df.sort_values(['dataset', 'human_repair_num'])
    
    output_file = os.path.join(RESULTS_PATH, 'zeroec_evaluation_results.csv')
    df.to_csv(output_file, index=False)
    print(f"\n{'='*80}")
    print(f"Results saved to: {output_file}")
    print(f"{'='*80}")
    
    # Summary statistics - aggregate counts first, then calculate metrics
    print("\nSUMMARY BY HUMAN_REPAIR_NUM:")
    print("-" * 80)
    
    summary_data = []
    for human_repair_num in sorted(df['human_repair_num'].unique()):
        subset = df[df['human_repair_num'] == human_repair_num]
        
        # Sum the raw counts
        total_tp = subset['tp'].sum()
        total_fp = subset['fp'].sum()
        total_fn = subset['fn'].sum()
        total_errors = subset['total_errors'].sum()
        total_time = subset['execution_time'].sum()
        
        # Calculate overall metrics from aggregated counts
        overall_precision = total_tp / (total_tp + total_fp) if (total_tp + total_fp) > 0 else 0.0
        overall_recall = total_tp / (total_tp + total_fn) if (total_tp + total_fn) > 0 else 0.0
        overall_f1 = 2 * overall_precision * overall_recall / (overall_precision + overall_recall) if (overall_precision + overall_recall) > 0 else 0.0
        
        summary_data.append({
            'human_repair_num': human_repair_num,
            'tp': total_tp,
            'fp': total_fp,
            'fn': total_fn,
            'total_errors': total_errors,
            'precision': round(overall_precision, 4),
            'recall': round(overall_recall, 4),
            'f1': round(overall_f1, 4),
            'total_time': round(total_time, 2),
            'avg_time_per_dataset': round(total_time / len(subset), 2)
        })
    
    summary = pd.DataFrame(summary_data)
    print(summary.to_string(index=False))
    
    summary_file = os.path.join(RESULTS_PATH, 'zeroec_evaluation_summary.csv')
    summary.to_csv(summary_file)
    print(f"\nSummary saved to: {summary_file}")
    
    # Per-dataset results
    print(f"\n{'='*80}")
    print("PER-DATASET RESULTS:")
    print("-" * 80)
    for dataset in sorted(df['dataset'].unique()):
        print(f"\n{dataset}:")
        subset = df[df['dataset'] == dataset][
            ['human_repair_num', 'precision', 'recall', 'f1', 'tp', 'fp', 'fn', 'execution_time']
        ]
        print(subset.to_string(index=False))


if __name__ == '__main__':
    main()
